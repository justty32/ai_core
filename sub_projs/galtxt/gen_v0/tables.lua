-- intent 成本（L1 搜尋的「劇本意圖」項）：呼叫方只說要甜/要靜，座標交給搜尋
------------------------------------------------------------------
T.intent_cost = {
  ["甜"] = function(topic, coord)
    return (coord.ending == "T10" and 0 or 2)
         + ((coord.gap == "C2" or coord.gap == "C4") and 0 or 1)
  end,
  ["靜"] = function(topic, coord)
    return (topic.temp == "靜" and 0 or 2)
         + (coord.ending == "T9" and 0 or 2)
  end,
}

